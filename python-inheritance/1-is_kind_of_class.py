#!/usr/bin/python3
"""
Check if an object is an instance of, or if the obj\
is an instance of a class inheriting from a_class.
"""


def is_kind_of_class(obj, a_class):
    """
    Check if an object is an instance of or if the obj\
    is an instance of a class inheriting from a_class.
    """
    return isinstance(obj, a_class)
